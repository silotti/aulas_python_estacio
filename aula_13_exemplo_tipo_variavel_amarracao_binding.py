#binding (amarração), pode alterar o tempo de execução
#exemplo de troca de tipo da variável no python binding

#<class 'int'>
v_dado = 10
print(v_dado, type(v_dado))

#Trocando para:<class 'float'>
v_dado = 3.14
print(v_dado, type(v_dado))

#Trocando para:<class 'str'>
v_dado = "Python"
print(v_dado, type(v_dado))

#Trocando para:<class 'bool'>
v_dado = True
print(v_dado, type(v_dado))
